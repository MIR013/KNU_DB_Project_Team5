DROP TABLE IF EXISTS SHOPPINGBAG CASCADE;
DROP TABLE IF EXISTS INCLUDE CASCADE;



CREATE TABLE CUSTOMER(
Customer_id	VARCHAR(15)	NOT NULL,
Cutomer_pwd	VARCHAR(15)	NOT NULL,
Name		VARCHAR(15),
Address		VARCHAR(20)	NOT NULL,
Phone_number	CHAR(11)	NOT NULL,
Job		VARCHAR(15),
Type		CHAR(1),
Sex		CHAR(1),
Age		INT,
C_shipname	VARCHAR(10),
PRIMARY KEY(Customer_id)
);

CREATE TABLE SHOPPINGBAG(
Customer_id	VARCHAR(15) 	NOT NULL,
Shoppingbag_no	INT		NOT NULL,
PRIMARY KEY(Shoppingbag_no,Customer_id)
);

CREATE TABLE BRAND(
Brand_name	VARCHAR(15)	NOT NULL,
Supplier_name	VARCHAR(10)	NOT NULL,
PRIMARY KEY(Brand_name)
);

CREATE TABLE CATEGORY(
Category_no	INT		NOT NULL,
Large_category	VARCHAR(15)	NOT NULL,
Middle_category	VARCHAR(15)	NOT NULL,
Small_category	VARCHAR(15)	NOT NULL,
PRIMARY KEY(Category_no)
);

CREATE TABLE ITEM(
Item_id		VARCHAR(15)	NOT NULL,
Item_name	VARCHAR(15)	NOT NULL,
Standard_price	INT,
Keep_num	INT,
Relese_unit	INT,
Item_brand_name	VARCHAR(15),
Category_no	INT,
PRIMARY KEY(Item_id)
);

CREATE TABLE INCLUDE(
Shoppingbag_no	INT		NOT NULL,
Customer_id	VARCHAR(15) 	NOT NULL,
Item_id		VARCHAR(15),
Item_num	INT,
PRIMARY KEY(Shoppingbag_no,Customer_id,Item_id)
);

CREATE TABLE SUPPLIER(
Supplier_name	VARCHAR(10)	NOT NULL,
PRIMARY KEY(Supplier_name)
);

CREATE TABLE SHIPPINGCOMPANY(
Shippingcomp_name	VARCHAR(10)	NOT NULL,
Shippingcomp_count	INT		DEFAULT 0,
PRIMARY KEY(Shippingcomp_name)
);

CREATE TABLE SHIPPINGCOMPANY_LOCATION(
Shippingcomp_name	VARCHAR(10),
Shippingcomp_location	VARCHAR(10),
PRIMARY KEY(Shippingcomp_name,Shippingcomp_location)
);


DROP TABLE IF EXISTS ORDERS CASCADE;
CREATE TABLE ORDERS(
Order_no		INT		NOT NULL,
Order_date	DATE		NOT NULL,
Customer_id 	VARCHAR(15) 	NOT NULL,
Item_id		VARCHAR(15) 	NOT NULL,
Item_num	INT		NOT NULL,
PRIMARY KEY(Customer_id,Item_id)
);



